# Praktek SET
print("====================")
set1 = {1, 2, 3}
set2 = {3, 4, 5}

gabung = set1.union(set2)  # Gabung elemen set
print("Hasil Gabungan set 1 & 2 = ",gabung)  # Output: {1, 2, 3, 4, 5}

irisan = set1.intersection(set2)  # irisan angka
print("Hasil irisan Angka = ",irisan)  # Output: {3}
