# Praktek String 
print("=====================")
teks = input("Masukkan Nama Anda :")
print(teks.lower())   # Output: huruf kecil semua
print(teks.upper())   # Output: huruf KAPITAL
print(teks.strip())   # Output: huruf Awal Kata besar
print(teks.replace(" ","_")) # Output: antar kata ada _
print(teks.split(";")) # Output: pemisah koma
print(teks[2:]) # Mulai dari indkes 2
print(teks[2:8]) # Mulai dari indkes 2 sampai indeks 8
print(teks + "MERDEKA")
